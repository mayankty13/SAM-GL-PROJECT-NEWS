import matplotlib.pyplot as plt
from collections import Counter
import pandas as pd
import seaborn as sns
from wordcloud import WordCloud # Import WordCloud

def tokenize_and_filter(text):
    return [token for token in normalize_text(text).split() if token not in STOP_WORDS and len(token) > 1]

def top_n_words(series, n=25):
    counts = Counter()
    for value in series:
        tokens = tokenize_and_filter(value)
        counts.update(tokens)
    return counts.most_common(n)

def plot_top_words_by_category(df_to_plot, n=15):
    categories = df_to_plot['category'].unique()
    n_cols = 2 # Number of columns for subplots
    n_rows = (len(categories) + n_cols - 1) // n_cols # Calculate rows needed

    fig, axes = plt.subplots(n_rows, n_cols, figsize=(15, 6 * n_rows), constrained_layout=True)
    axes = axes.flatten() # Flatten in case of single row/column

    for i, category in enumerate(categories):
        subset = df_to_plot[df_to_plot['category'] == category]
        top_words_list = top_n_words(subset['content_clean'], n=n)
        words, counts = zip(*top_words_list)

        sns.barplot(x=list(counts), y=list(words), ax=axes[i], palette='viridis', hue=list(words), legend=False)
        axes[i].set_title(f'Top {n} words in {category} articles')
        axes[i].set_xlabel('Count')
        axes[i].set_ylabel('Word')

    # Hide any unused subplots
    for j in range(i + 1, len(axes)):
        fig.delaxes(axes[j])

    fig.suptitle('Most Common Words by Category', fontsize=18, y=1.02)
    plt.show()
    plt.close(fig)

def generate_word_clouds(df_to_plot):
    print_section('Generating Word Clouds')
    categories = df_to_plot['category'].unique()
    for category in categories:
        subset_text = " ".join(df_to_plot[df_to_plot['category'] == category]['content_clean'])
        if subset_text:
            wordcloud = WordCloud(width=800, height=400, background_color='white').generate(subset_text)
            plt.figure(figsize=(10, 5))
            plt.imshow(wordcloud, interpolation='bilinear')
            plt.axis('off')
            plt.title(f'Word Cloud for {category.capitalize()} Articles')
            plt.show()
            plt.close()
        else:
            print(f"No clean content found for category: {category}")


