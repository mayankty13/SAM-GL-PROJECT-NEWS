"""## 3. Data Pre-processing
- Remove stopwords (common words like "and," "the" which don’t contribute to meaning)
- Handle punctuation, special characters, numbers, and other non-alphabetic symbols
- Perform case normalization (convert text to lowercase)
- Handle missing values in the description or content fields. This could include dropping rows or filling missing values with a placeholder
- Identify and handle articles with unusually short or long content (e.g., less than 5 words could be an outlier)
- Transform text data into numerical format using vectorization techniques (e.g., TF-IDF, Bag of Words, or word embeddings)

### Select VECTORIZATION_METHOD
#### options: 'bow', 'tfidf', 'embeddings'
"""

VECTORIZATION_METHOD = 'embeddings'  # options: 'bow', 'tfidf', 'embeddings'

STOP_WORDS = sklearn_text.ENGLISH_STOP_WORDS
DROP_OUTLIERS = True
MIN_CONTENT_WORDS = 5
MAX_CONTENT_WORDS = 5000
MAX_FEATURES = 10000
EMBEDDING_SIZE = 100

def normalize_text(text, remove_stopwords=True):
    if pd.isna(text):
        return ''
    text = str(text)
    text = re.sub(r'http\S+|www\S+', ' ', text)
    text = re.sub(r'<[^>]+>', ' ', text)
    text = re.sub(r'[^a-zA-Z\s]', ' ', text)
    text = text.lower()
    tokens = text.split()
    if remove_stopwords:
        tokens = [token for token in tokens if token not in STOP_WORDS and len(token) > 1]
    else:
        tokens = [token for token in tokens if len(token) > 1]
    return ' '.join(tokens)

def compute_length_features(df_to_process):
    df_to_process['headline_length'] = df_to_process['headlines'].fillna('').astype(str).apply(len)
    df_to_process['headline_word_count'] = df_to_process['headlines'].fillna('').astype(str).apply(lambda s: len(s.split()))
    df_to_process['description_length'] = df_to_process['description'].fillna('').astype(str).apply(len)
    df_to_process['description_word_count'] = df_to_process['description'].fillna('').astype(str).apply(lambda s: len(s.split()))
    df_to_process['content_length'] = df_to_process['content'].fillna('').astype(str).apply(len)
    df_to_process['content_word_count'] = df_to_process['content'].fillna('').astype(str).apply(lambda s: len(s.split()))
    return df_to_process

def preprocess_dataframe(df_to_process):
    df_to_process = df_to_process.copy()
    for column in ['headlines', 'description', 'content']:
        df_to_process[column] = df_to_process[column].fillna('').astype(str)
    df_to_process['category'] = df_to_process['category'].astype(str).str.strip().str.lower()

    df_to_process = compute_length_features(df_to_process)
    df_to_process['is_short_content'] = df_to_process['content_word_count'] < MIN_CONTENT_WORDS
    df_to_process['is_long_content'] = df_to_process['content_word_count'] > MAX_CONTENT_WORDS
    df_to_process['content_outlier'] = df_to_process['is_short_content'] | df_to_process['is_long_content']

    if DROP_OUTLIERS:
        before = len(df_to_process)
        df_to_process = df_to_process[~df_to_process['content_outlier']].copy()
        print(f'Dropped {before - len(df_to_process)} outlier rows based on content length.')

    df_to_process['headlines_clean'] = df_to_process['headlines'].apply(lambda x: normalize_text(x, remove_stopwords=True))
    df_to_process['description_clean'] = df_to_process['description'].apply(lambda x: normalize_text(x, remove_stopwords=True))
    df_to_process['content_clean'] = df_to_process['content'].apply(lambda x: normalize_text(x, remove_stopwords=True))
    df_to_process['text'] = (
        df_to_process['headlines_clean'] + ' ' +
        df_to_process['description_clean'] + ' ' +
        df_to_process['content_clean']
    ).str.strip()
    df_to_process['tokens'] = df_to_process['text'].apply(lambda x: x.split())
    return df_to_process # Corrected: return df_to_process instead of df

def build_vectorizer(df_to_vectorize):
    if VECTORIZATION_METHOD == 'bow':
        vectorizer = CountVectorizer(
            strip_accents='unicode',
            stop_words='english',
            max_features=MAX_FEATURES,
            ngram_range=(1, 2),
        )
        X = vectorizer.fit_transform(df_to_vectorize['text'])
        print('BoW matrix shape:', X.shape)
        print('Sample BoW feature names:', vectorizer.get_feature_names_out()[:20])
        return X, vectorizer

    if VECTORIZATION_METHOD == 'tfidf':
        vectorizer = TfidfVectorizer(
            strip_accents='unicode',
            stop_words='english',
            max_features=MAX_FEATURES,
            ngram_range=(1, 2),
        )
        X = vectorizer.fit_transform(df_to_vectorize['text'])
        print('TF-IDF matrix shape:', X.shape)
        print('Sample TF-IDF feature names:', vectorizer.get_feature_names_out()[:20])
        return X, vectorizer

    if VECTORIZATION_METHOD == 'embeddings':
        embedding_vectorizer = TfidfVectorizer(
            strip_accents='unicode',
            stop_words='english',
            max_features=MAX_FEATURES,
            ngram_range=(1, 2),
        )
        X_sparse = embedding_vectorizer.fit_transform(df_to_vectorize['text'])
        n_components = min(EMBEDDING_SIZE, X_sparse.shape[1] - 1)
        svd = TruncatedSVD(n_components=n_components, random_state=42)
        X = svd.fit_transform(X_sparse)
        print('Embedding matrix shape:', X.shape)
        np.save('article_embeddings.npy', X)
        return X, {'vectorizer': embedding_vectorizer, 'svd': svd}

    raise ValueError(f'Unsupported vectorization method: {VECTORIZATION_METHOD}')

